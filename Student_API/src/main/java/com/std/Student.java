package com.std;

public class Student {
	private String std_rollno;
	private String std_name;
	private String std_city;
	private String std_class;
	private String std_gender;
	public String getStd_rollno() {
		return std_rollno;
	}
	public void setStd_rollno(String std_rollno) {
		this.std_rollno = std_rollno;
	}
	public String getStd_name() {
		return std_name;
	}
	public void setStd_name(String std_name) {
		this.std_name = std_name;
	}
	public String getStd_city() {
		return std_city;
	}
	public void setStd_city(String std_city) {
		this.std_city = std_city;
	}
	public String getStd_class() {
		return std_class;
	}
	public void setStd_class(String std_class) {
		this.std_class = std_class;
	}
	public String getStd_gender() {
		return std_gender;
	}
	public void setStd_gender(String std_gender) {
		this.std_gender = std_gender;
	}
}
